# ── Load model & dataset ─────────────────────────────────────
model  = joblib.load(MODEL_PATH)
raw_df = pd.read_excel(DATA_PATH)

# Replicate notebook preprocessing: fill NaN with column means first
training_df = raw_df.copy()
training_df.fillna(training_df.mean(numeric_only=True), inplace=True)

feature_means = training_df[FEATURE_COLUMNS].mean(numeric_only=True).to_dict()
feature_stds  = training_df[FEATURE_COLUMNS].std(numeric_only=True).to_dict()
feature_mins  = training_df[FEATURE_COLUMNS].min(numeric_only=True).to_dict()
feature_maxs  = training_df[FEATURE_COLUMNS].max(numeric_only=True).to_dict()

scores     = training_df["Performance_Score"]
SCORE_MIN  = float(scores.min())
SCORE_MAX  = float(scores.max())
SCORE_MEAN = float(scores.mean())
SCORE_STD  = float(scores.std())

# Tier distribution from actual dataset
TIER_DIST = {
    "Excellent":     int((scores >= 50).sum()),
    "Good":          int(((scores >= 42) & (scores < 50)).sum()),
    "Below Average": int(((scores >= 35) & (scores < 42)).sum()),
    "Poor":          int((scores < 35).sum()),
}

# Model evaluation — same split as notebook (random_state=42, test_size=0.2)
X_full = training_df[FEATURE_COLUMNS]
y_full = training_df["Performance_Score"]
_, X_te, _, y_te = train_test_split(X_full, y_full, test_size=0.2, random_state=42)
y_pred_te = model.predict(X_te.values)

MODEL_R2   = round(float(r2_score(y_te, y_pred_te)), 4)
MODEL_RMSE = round(float(mean_squared_error(y_te, y_pred_te) ** 0.5), 4)
N_TRAIN    = int(len(X_full) - len(X_te))
N_TEST     = int(len(X_te))


# ── Helpers ───────────────────────────────────────────────────
def _safe_float(val, fallback):
    """Convert val to float; return fallback on failure or non-finite result."""
    if val == "" or val is None:
        return fallback
    try:
        v = float(val)
        return v if (v == v and abs(v) != float("inf")) else fallback
    except (ValueError, TypeError):
        return fallback


def predict_performance(data: dict):
    """
    Build a numpy row from user-supplied data (empty → dataset mean),
    then run model inference.
    Returns (prediction: float, input_row: list[float])
    """
    row = [_safe_float(data.get(col, ""), feature_means[col]) for col in FEATURE_COLUMNS]
    X   = np.array(row, dtype=float).reshape(1, -1)
    return round(float(model.predict(X)[0]), 2), row


def get_tier(score: float):
    if score >= 50:
        return "Excellent", (
            "This branch is performing at an exceptional level, "
            "significantly above the portfolio average."
        )
    elif score >= 42:
        return "Good", (
            "This branch is performing above average with solid "
            "operational and financial metrics."
        )
    elif score >= 35:
        return "Below Average", (
            "Performance is below the portfolio average. "
            "Key operational areas require targeted improvement."
        )
    else:
        return "Poor", (
            "This branch requires immediate management intervention "
            "and a structured turnaround plan."
        )


def score_percentile(score: float) -> float:
    """Percentage of dataset branches that score below `score`."""
    return round(float((scores < score).mean() * 100), 1)


