@app.route("/api/predict", methods=["POST"])
def api_predict():
    data = request.get_json(silent=True) or {}
    try:
        form_values = {col: data.get(col, "") for col in FEATURE_COLUMNS}
        prediction, input_row = predict_performance(form_values)
        tier, desc = get_tier(prediction)
        pct        = round((prediction - SCORE_MIN) / (SCORE_MAX - SCORE_MIN) * 100, 2)
        percentile = score_percentile(prediction)

        # Feature comparison: z-score vs dataset
        feature_comparison = []
        for col, user_val in zip(FEATURE_COLUMNS, input_row):
            mean = feature_means[col]
            std  = feature_stds.get(col, 1) or 1
            z    = (user_val - mean) / std
            feature_comparison.append({
                "feature":    col.replace("_", " "),
                "key":        col,
                "user_value": round(user_val, 4),
                "mean":       round(mean, 4),
                "z_score":    round(z, 3),
                "is_default": data.get(col, "") == "",
            })
        feature_comparison.sort(key=lambda x: abs(x["z_score"]), reverse=True)

        return jsonify({
            "prediction":        prediction,
            "tier":              tier,
            "description":       desc,
            "score_percent":     pct,
            "percentile":        percentile,
            "score_min":         SCORE_MIN,
            "score_max":         SCORE_MAX,
            "score_mean":        round(SCORE_MEAN, 2),
            "score_std":         round(SCORE_STD, 2),
            "model_r2":          MODEL_R2,
            "model_rmse":        MODEL_RMSE,
            "n_train":           N_TRAIN,
            "n_test":            N_TEST,
            "n_samples":         int(len(training_df)),
            "tier_dist":         TIER_DIST,
            "feature_comparison": feature_comparison[:12],
        })
    except Exception as exc:
        return jsonify({"error": str(exc)}), 400


@app.route("/api/sample-data", methods=["GET"])
def api_sample_data():
    row = training_df.sample(1).iloc[0]
    sample = {col: round(float(row[col]), 4) for col in FEATURE_COLUMNS if pd.notna(row[col])}
    if "Branch_ID" in training_df.columns:
        val = row["Branch_ID"]
        if pd.notna(val):
            try:
                # If it's a float like 1001.0, convert to int to display nicely
                sample["Branch_ID"] = int(val) if float(val).is_integer() else val
            except:
                sample["Branch_ID"] = str(val)
        else:
            sample["Branch_ID"] = ""
    return jsonify(sample)


@app.route("/api/branch-data/<branch_id>", methods=["GET"])
def api_branch_data(branch_id):
    try:
        # Convert to float first, then int if possible (e.g. "1871" or "1871.0" -> 1871)
        b_id = int(float(branch_id))
    except ValueError:
        b_id = str(branch_id).strip()

    # Search by direct matching
    matched = training_df[training_df["Branch_ID"] == b_id]
    if len(matched) == 0:
        # Fallback to string matching
        matched = training_df[training_df["Branch_ID"].astype(str).str.strip() == str(branch_id).strip()]

    if len(matched) == 0:
        return jsonify({"error": f"Branch '{branch_id}' not found in dataset"}), 404

    row = matched.iloc[0]
    sample = {col: round(float(row[col]), 4) for col in FEATURE_COLUMNS if pd.notna(row[col])}
    
    # Format Branch_ID nicely
    val = row["Branch_ID"]
    if pd.notna(val):
        try:
            sample["Branch_ID"] = int(val) if float(val).is_integer() else val
        except:
            sample["Branch_ID"] = str(val)
    else:
        sample["Branch_ID"] = branch_id

    return jsonify(sample)




